    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Ingreso de datos
        
        try {
            //Este driver establecera una conexion con el controlador de la base de datos especificado en la url del jdbc
            Connection cn=DriverManager.getConnection("jdbc:mysql://localhost/Film_Magic","root", "admin");
            //utilizado para declarar una sentencia lo que se encarga es definir una consulta a sql a ejecutar contra el motor de la base de datos
            PreparedStatement pst= cn.prepareStatement("insert into Cliente values(?,?,?,?,?,?,?,?)");
            
            pst.setString(1, txt_codigocliente.getText().trim());// .trim() Método que elimina los caracteres blancos iniciales y finales de la cadena, devolviendo una copia de la misma.
            pst.setString(2, txt_cui.getText().trim());
            pst.setString(3, txt_nombre.getText().trim());
            pst.setString(4, txt_apellido.getText().trim());
            pst.setString(5, txt_telefono.getText().trim());
            pst.setString(6, txt_cargo.getText().trim());
            pst.setString(7, txt_estado.getText().trim());
            pst.setString(8, txt_codigoempleado.getText().trim());

            
            pst.executeUpdate(); //Ejecuta la instrucción SQL dada, que puede ser una instrucción INSERT, UPDATE, MERGE o DELETE; o una instrucción SQL que no devuelve nada, como una instrucción DDL de SQL.
            
            txt_codigocliente.setText("");
            txt_cui.setText("");
            txt_nombre.setText("");
            txt_apellido.setText("");
            txt_telefono.setText("");
            txt_estado.setText("");
            txt_cargo.setText("");
            txt_codigoempleado.setText("");
            
            Label_status.setText("Registro Exitoso.");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Modificar datos
        try {
            String ID= txt_buscar.getText().trim();
            
            Connection cn=DriverManager.getConnection("jdbc:mysql://localhost/Film_Magic","root", "admin");
            PreparedStatement pst= cn.prepareStatement("update Cliente set Codigo_Cliente=?, CUI_Cliente=?, Nombre_Cliente=?, Apellido_Cliente=?, Telefono_Cliente=?, Estado_Tarjeta_Cliente=?, Codigo_Empleado=? where Codigo_Cliente= "+ID);
            
            pst.setString(1, txt_codigocliente.getText().trim());
            pst.setString(2, txt_cui.getText().trim());
            pst.setString(3, txt_nombre.getText().trim());
            pst.setString(4, txt_apellido.getText().trim());
            pst.setString(5, txt_telefono.getText().trim());
            pst.setString(6, txt_estado.getText().trim());
            pst.setString(7, txt_codigoempleado.getText().trim());

            
            pst.executeUpdate();
            
            Label_status.setText("Modificacion Exitosa.");
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //buscar Datos

        try {
            Connection cn=DriverManager.getConnection("jdbc:mysql://localhost/Film_Magic","root", "admin");
            PreparedStatement pst= cn.prepareStatement("select * from Cliente where Codigo_Cliente= ?");
            
            pst.setString(1, txt_buscar.getText().trim());
            ResultSet rs= pst.executeQuery();
            
            if (rs.next()) {
                txt_codigocliente.setText(rs.getString("Codigo_Cliente"));
                txt_cui.setText(rs.getString("CUI_Cliente"));
                txt_nombre.setText(rs.getString("Nombre_Cliente"));
                txt_apellido.setText(rs.getString("Apellido_Cliente"));
                txt_telefono.setText(rs.getString("Telefono_Cliente"));
                txt_estado.setText(rs.getString("Estado_Tarjeta_Cliente"));
                txt_codigoempleado.setText(rs.getString("Codigo_Empleado"));
            }else
                JOptionPane.showMessageDialog(null, "Registro no Encontrado.");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton4ActionPerformed

